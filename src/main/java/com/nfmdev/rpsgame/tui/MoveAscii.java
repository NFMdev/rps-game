    // TODO: separate MoveArt from normalization
    public String renderMoveAscii(Move move) {
        return switch (move) {
            case Move.ROCK -> normalize(ROCK, WIDTH, HEIGHT);
            case Move.PAPER -> normalize(PAPER, WIDTH, HEIGHT);
            case Move.SCISSORS -> normalize(SCISSORS, WIDTH, HEIGHT);
        };
    }

    public String normalize(String raw, int width, int height) {
        List<String> lines = raw.stripTrailing()
                .lines()
                .map(MoveAscii::replaceUnsafeSpaces)
                .map(MoveAscii::validate)
                .map(line -> fit(line, width))
                .collect(Collectors.toCollection(ArrayList::new));

        while (lines.size() < height) {
            lines.add(" ".repeat(width));
        }

        if (lines.size() > height) {
            lines = lines.subList(0, height);
        }

        return String.join(System.lineSeparator(), lines);
    }

    private static String replaceUnsafeSpaces(String line) {
        return line.replace('в Җ', ' ');
    }

    private static String validate(String line) {
        if (line.contains("\t")) {
            throw new IllegalArgumentException("Art must not contain tabs");
        }

        return line;
    }

    private static String fit(String line, int width) {
        if (line.length() > width) {
            return line.substring(0, width);
        }

        return String.format("%-" + width + "s", line);
    }
